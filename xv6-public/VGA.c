/* Escribe en los registros del arreglo regs
*/
void
write_regs(uchar *regs)
{
  /* write MISCELLANEOUS reg */
  outb(VGA_MISC_WRITE, *regs);
  regs++;
  /* write SEQUENCER regs */
  for(uint i = 0; i < VGA_NUM_SEQ_REGS; i++){
    outb(VGA_SEQ_INDEX, i);
    outb(VGA_SEQ_DATA, *regs);
    regs++;
  }
  /* unlock CRTC registers */
  outb(VGA_CRTC_INDEX, 0x03);
  outb(VGA_CRTC_DATA, inb(VGA_CRTC_DATA) | 0x80);
  outb(VGA_CRTC_INDEX, 0x11);
  outb(VGA_CRTC_DATA, inb(VGA_CRTC_DATA) & ~0x80);
  /* make sure they remain unlocked */
  // (milagro) No dejen codigo muerto
  // regs[0x03] |= 0x80;
  // regs[0x11] &= ~0x80;
  /* write CRTC regs */
  for(uint i = 0; i < VGA_NUM_CRTC_REGS; i++){
    outb(VGA_CRTC_INDEX, i);
    outb(VGA_CRTC_DATA, *regs);
    regs++;
  }
  /* write GRAPHICS CONTROLLER regs */
  for(uint i = 0; i < VGA_NUM_GC_REGS; i++){
    outb(VGA_GC_INDEX, i);
    outb(VGA_GC_DATA, *regs);
    regs++;
  }
  /* write ATTRIBUTE CONTROLLER regs */
  for(uint i = 0; i < VGA_NUM_AC_REGS; i++){
    (void)inb(VGA_INSTAT_READ);
    outb(VGA_AC_INDEX, i);
    outb(VGA_AC_WRITE, *regs);
    regs++;
  }
  /* lock 16-color palette and unblank display */
  (void)inb(VGA_INSTAT_READ);
  outb(VGA_AC_INDEX, 0x20);
}

static void
set_plane(uint p)
{
  p &= 3;
  uchar pmask = 1 << p;
  /* set read plane */
  outb(VGA_GC_INDEX, 4);
  outb(VGA_GC_DATA, p);
  /* set write plane */
  outb(VGA_SEQ_INDEX, 2);
  outb(VGA_SEQ_DATA, pmask);
}

/* write font to plane P4 (assuming planes are named P1, P2, P4, P8)
 */
void
write_font(uchar *buf, uint font_height)
{
  /* save registers
  set_plane() modifies GC 4 and SEQ 2, so save them as well */
  outb(VGA_SEQ_INDEX, 2);
  uchar seq2 = inb(VGA_SEQ_DATA);

  outb(VGA_SEQ_INDEX, 4);
  uchar seq4 = inb(VGA_SEQ_DATA);
  /* turn off even-odd addressing (set flat addressing)
  assume: chain-4 addressing already off */
  outb(VGA_SEQ_DATA, seq4 | 0x04);

  outb(VGA_GC_INDEX, 4);
  uchar gc4 = inb(VGA_GC_DATA);

  outb(VGA_GC_INDEX, 5);
  uchar gc5 = inb(VGA_GC_DATA);
  /* turn off even-odd addressing */
  outb(VGA_GC_DATA, gc5 & ~0x10);

  outb(VGA_GC_INDEX, 6);
  uchar gc6 = inb(VGA_GC_DATA);
  /* turn off even-odd addressing */
  outb(VGA_GC_DATA, gc6 & ~0x02);
  /* write font to plane P4 */
  set_plane(2);
  /* write font 0 */
  for(uint i = 0; i < 256; i++){
    for (uint j = 0; j < font_height; j++){
      // VGA_MEMBASE
      ((uchar*)VGA_font_array)[32*i + j] = *buf;
      buf++;
    }
  }

  /* restore registers */
  outb(VGA_SEQ_INDEX, 2);
  outb(VGA_SEQ_DATA, seq2);
  outb(VGA_SEQ_INDEX, 4);
  outb(VGA_SEQ_DATA, seq4);
  outb(VGA_GC_INDEX, 4);
  outb(VGA_GC_DATA, gc4);
  outb(VGA_GC_INDEX, 5);
  outb(VGA_GC_DATA, gc5);
  outb(VGA_GC_INDEX, 6);
  outb(VGA_GC_DATA, gc6);
}

static void
VGA_set_palette_color(int index, int r, int g, int b)
{
  outb(VGA_DAC_WRITE_INDEX, index);
  outb(VGA_DAC_DATA, r);
  outb(VGA_DAC_DATA, g);
  outb(VGA_DAC_DATA, b);
}

/* Seetea la paleta del VGA a palette
 * palette debe tener 256 elementos */
void
VGA_set_palette(int* palette)
{
  for(int index = 0; index < 256; index++){
    int value = palette[index];
    VGA_set_palette_color
      (index, (value>>18) & 0x3f, (value>>10) & 0x3f, (value>>2) & 0x3f);
  }
}


bool
mode_is_text(VGA_mode mode)
{
  return(mode == VGA_text_80x25);
}

bool
mode_is_graphic(VGA_mode mode)
{
  return(mode == VGA_graphic_320x200x256);
}

/* Retorna el ancho del modo, tanto si es modo texto como gráfico
 * si en un modo invalido retorna 0
 */
int
mode_width(VGA_mode mode)
{
  int res = 0;
  switch(mode){
  case VGA_text_80x25:
    res = 80;
    break;
  case VGA_graphic_320x200x256:
    res = 320;
    break;
  }
  return(res);
}

/* Retorna la altura del modo, tanto si es modo texto como gráfico
 * si en un modo invalido retorna 0
 */
int
mode_height(VGA_mode mode)
{
  int res = 0;
  switch(mode){
  case VGA_text_80x25:
    res = 25;
    break;
  case VGA_graphic_320x200x256:
    res = 200;
    break;
  }
  return(res);
}

