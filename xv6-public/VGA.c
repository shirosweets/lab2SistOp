/* Retorna la dirección de memoría del comienzo del buffer cuando mode
 * es el modo
 */
void*
mode_buffer(VGA_mode mode)
{
  void* res = 0;
  switch(mode){
  case VGA_text_80x25:
    res = buffer_text_80x25;
    break;
  case VGA_graphic_320x200x256:
    res = buffer_graphic_320x200;
    break;
  }
  return(res);

}

/* Variable para saber el modo actual
 * Cada ves que se cambia de modo se la actualiza
 */
VGA_mode actual_mode = VGA_text_80x25;

typedef struct{char s_ASCII_code; uchar s_atributes;} VGA_char;

static void
VGA_text_plot_letter(int x, int y, char letter, char atributes)
{
  int width = mode_width(actual_mode);
  int height = mode_height(actual_mode);
  VGA_char* charbuffer = mode_buffer(actual_mode);

  if(0 <= x && x < width && 0 <= y && y < height && mode_is_text(actual_mode))
    charbuffer[x + y*width] = (VGA_char){letter, atributes};
}

static void
VGA_text_put_string(int x, int y, char* str, char atributes)
{
  if(str != NULL) {
    for(uint i = 0u; str[i] != '\0'; i++) {
      VGA_text_plot_letter(x, y, str[i], atributes);
      x++;
    }
  }
}

/* Si el modo actual es de texto dibuja un pie de pantalla que dice SO2021
 */
void
vgainit(void)
{
  if(mode_is_text(actual_mode)){
    // Pintar el fondo
    int width = mode_width(actual_mode);
    for (int x = 0; x < width; x++) {
      VGA_text_plot_letter(x, 0, ' ', 0x20);
    }

    VGA_text_put_string(width/2 - 3, 0, "SO2021", 0x2f);
  }
}

/* Llamada al sistema que cambia de modo al modo dado,
 * si es un modo invalido no hace nada
 *
 * Cosas a tener en cuenta:
 *   Al cambiar a modo gráfico la memoría no se inicializa
 *   AL cambiar a modo texto si se inicializa
 */
void
VGA_mode_switch(VGA_mode mode)
{
  if(mode_is_text(mode)){
    // Cambio de modo
    write_regs(VGA_modes[mode]);
    actual_mode = mode;

    // Se ponen las fuentes
    write_font(g_8x16_font, 16);

    // Se inicializa todo
    int rows = mode_height(mode);
    int cols = mode_width(mode);
    for(int i = 0; i < cols; i++){
      for(int j = 0; j < rows; j++){
        VGA_text_plot_letter(i, j, ' ', 0x00);
      }
    }

    // Se llama a vgainit para que aparezca el pie de pantalla
    vgainit();
  }
  else if(mode_is_graphic(mode)){
    write_regs(VGA_modes[mode]);
    actual_mode = mode;
    VGA_set_palette(VGA_palette_256);
  }
}

/* Si las cordenadas son dentro de la pantalla (de 320x200) pinta el pixel,
 * si están afuera no hace nada.
 *
 * Si no está en modo gráfico no hace nada
 */
void
VGA_plot_pixel(int x, int y, uchar color)
{
  int width = mode_width(actual_mode);
  int height = mode_height(actual_mode);

  if(0 <= x && x < width && 0 <= y && y < height
    && mode_is_graphic(actual_mode)){
    uchar* framebuffer = mode_buffer(actual_mode);
    framebuffer[y * width + x] = color;
  }
}

/* Copia mode_width(actual_mode)*mode_height(actual_mode) bytes de buffer+
 * al arreglo de la pantalla
 *
 * Si no se está en modo gráfico no hace nada
 */
void
VGA_plot_screen(uchar* buffer)
{
  if(mode_is_graphic(actual_mode)){
    int width = mode_width(actual_mode);
    int height = mode_height(actual_mode);
    uchar* framebuffer = mode_buffer(actual_mode);

    for(int j = 0; j < width*height; j++){
      framebuffer[j] = buffer[j];
    }
  }
}

int
sys_VGA_mode_switch(void)
{
  int mode;
  if(argint(0, &mode) < 0)
    return -1;
  VGA_mode_switch((VGA_mode)mode);
  return 0;
}

int
sys_VGA_plot_pixel(void)
{
  int x, y, temp_color;
  if(argint(0, &x) < 0 || argint(1, &y) < 0 || argint(2, &temp_color) < 0)
    return -1;
  uchar color = (char)temp_color;
  VGA_plot_pixel(x, y, color);
  return 0;
}

int
sys_VGA_plot_screen(void)
{
  char* buffer;
  if(argptr(0, &buffer, mode_width(actual_mode)*mode_height(actual_mode)) < 0)
    return -1;
  VGA_plot_screen((uchar*)buffer);
  return 0;
}


